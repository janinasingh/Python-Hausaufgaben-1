print("Welcome to our MoodChecker")
mood=input("What is your mood today? ")

if mood == "happy":
    print("It is great to see you happy!")

elif mood == "nervous":
    print("Take a deep breath 3 times.")

elif mood == "sad":
    print("Everything will be okay!")

elif mood == "excited":
    print("That's amazing!")

elif mood == "relaxed":
    print("What's better than that?")

else:
    print("I don't know this emotion!")